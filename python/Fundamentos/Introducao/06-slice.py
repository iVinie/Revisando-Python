gameName = 'Fifa 23'
gameDescription = '''
Fifa 23 é um jogo de futebol, desenvolvido pela EA Sports
Que possibilita jogar localmente ou online
'''

# STRING [inicio:fim] indici inicial: 0 | indice final -1

#Busque toda a string a partir da primeira posição 
print (gameName[0:])

#Busque toda a string até a ultima posição
print (gameName[:5])

#Busque da terceira até a ultima
print(gameName[2:])

#Obs.: [inicio:fim:passo]

#busque toda a string de 2 em 2 caractere
print(gameDescription[::2])