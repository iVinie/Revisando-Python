import math

#1 - número pi:
print(math.pi)
print(f'{math.pi:.2f}')#Formatado em 2 casas decimais

#2 - Número de Euler
print(math.e)

#3 - Arredondando números
num1 = 10.4
print(math.ceil(num1))#Arredonda pra cima
print(math.floor(num1))#Arredonda para baixo

#4 - Fatorial de um número
num2 = 7
print(math.factorial(num2))

#5 - Potência de um número
print(math.pow(5, 5))

#6 - Raiz Quadrada
print(math.sqrt(25))

#7 - MDC
print(math.gcd(20, 70))

#8 - Log
print(math.log(10))